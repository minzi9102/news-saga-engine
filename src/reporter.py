                    history_lines.append(f"#### ğŸ“… å†å²è„‰ç»œ: {linked_saga.title}")
                    for h_event in past_events[:5]:
                        history_lines.append(f"- `{h_event.date}` {h_event.title} [ğŸ”—]({h_event.source_url})")
