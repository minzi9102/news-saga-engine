        md_lines.append(f"")

        # 3. éå†æ–°é—»åˆ—è¡¨
        for idx, item in enumerate(briefing.news_items, 1):
            linked_saga = url_to_saga_map.get(item.url)

            # A. æ ‡é¢˜è¡Œ
            type_icon = "âš¡" if item.type == NewsType.FLASH_SUB else "ğŸ“°"
            summary_line = f"{idx:02d}. {type_icon} {item.title}"

